package name.yumao.ffxiv.chn.model;

public class TeemoUpdateVo {
	private String url;
	
	private String path;
	
	private Long size;
	
	private String hash;
	
	private Long time;
	
	public String getUrl() {
		return this.url;
	}
	
	public void setUrl(String url) {
		this.url = url;
	}
	
	public String getPath() {
		return this.path;
	}
	
	public void setPath(String path) {
		this.path = path;
	}
	
	public Long getSize() {
		return this.size;
	}
	
	public void setSize(Long size) {
		this.size = size;
	}
	
	public String getHash() {
		return this.hash;
	}
	
	public void setHash(String hash) {
		this.hash = hash;
	}
	
	public Long getTime() {
		return this.time;
	}
	
	public void setTime(Long time) {
		this.time = time;
	}
}
